-- 1990 VE 1995 YILLARI ARASINDA DOÐAN
-- MÜÞTERÝLERÝ ÇEKÝNÝZ. (1990 VE 1995 DAHÝL)

SELECT * FROM CUSTOMERS 
WHERE BIRTHDATE >= '1990-01-01'
AND BIRTHDATE <= '1995-12-31'

SELECT * FROM CUSTOMERS 
WHERE BIRTHDATE BETWEEN '1990-01-01'
AND '1995-12-31'

SELECT * FROM CUSTOMERS 
WHERE YEAR(BIRTHDATE) >= 1990
AND YEAR (BIRTHDATE) <= 1995

SELECT * FROM CUSTOMERS 
WHERE YEAR(BIRTHDATE) BETWEEN 1990
AND  1995
